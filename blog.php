				<a href="/blog/2015/08/26/7-questions-jan-goldstein-founder-and-executive-director-of-arts-on-the-block/" title="Permalink to 7 Questions: Jan Goldstein, Founder and Executive Director of Arts on the Block" rel="bookmark">7 Questions: Jan Goldstein, Founder and Executive Director of Arts on the Block</a>
